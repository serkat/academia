function my_pre_save_post( $post_id ) {

    // check if this is to be a new post
    if( $post_id != 'new_post' ) {

        return $post_id;

    }
    
    $post_title = $post_id.' variable';
    $post = array(
        'post_status'  => 'draft' ,
        'post_title'  => $post_title ,
        'post_type'  => 'addrezult' ,
    );  

    // insert the post
    $post_id = wp_insert_post( $post ); 

    // return the new ID
    return $post_id;

}

add_filter('acf/pre_save_post' , 'my_pre_save_post', 10, 1 );